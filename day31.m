w=0:0.01:pi;
num=[1];
den=[1 -0.5];
xw=freqz(num,den,w);
magx=abs(xw);
phasex=angle(xw);
realx=real(xw);
imagx=imag(xw);
subplot(2,2,1);
plot(w/pi,magx);
xlabel('w/pi');
ylabel('magx');
title('Magnitude');
subplot(2,2,2);
plot(w/pi,phasex);
xlabel('w/pi');
ylabel('phasex');
title('Phase');
subplot(2,2,3);
plot(w/pi,realx);
xlabel('w/pi');
ylabel('realx');
title('Real Part');
subplot(2,2,4);
plot(w/pi,imagx);
xlabel('w/pi');
ylabel('imagx');
title('Imaginary part');
