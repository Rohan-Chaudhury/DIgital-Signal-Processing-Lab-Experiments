x=[1 2 0 3];
lx=length(x);
n=0:lx-1;
n1=mod(-n,lx);
x1=x(n1+1);
xe=0.5*(x+x1);
xo=0.5*(x-x1);
subplot(2,2,1);
stem(n,x);
xlabel("time instance");
ylabel("amplitude");
title("signal");
subplot(2,2,2);
stem(n,x1);
xlabel("time instance");
ylabel("amplitude");
title("folded signal");
subplot(2,2,3);
stem(n,xe);
xlabel("time instance");
ylabel("amplitude");
title("even signal");
subplot(2,2,4);
stem(n,xo);
xlabel("time instance");
ylabel("amplitude");
title("odd signal");